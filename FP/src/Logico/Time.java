package Logico;

public class Time {

	private int horas;
	private int minutos;
	
	public Time(int horas, int minutos) {
		super();
		this.horas = horas;
		this.minutos = minutos;
	}

	public int getHoras() {
		return horas;
	}

	public int getMinutos() {
		return minutos;
	}

	public void setHoras(int horas) {
		this.horas = horas;
	}

	public void setMinutos(int minutos) {
		this.minutos = minutos;
	}
	
	
}
